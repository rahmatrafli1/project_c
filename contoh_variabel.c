#include <stdio.h>

int main()
{
    // membuat variabel dengan tipe data integer
    int berat;
    // membuat variabel dan langsung mengisinya
    int tinggi = 176;

    // mengisi nilai ke variabel
    berat = 100;

    // mencetak isi variabel ke layar
    printf("Berat badan saya adalah %ikg dan tinggi %icm", berat, tinggi);

    return 0;
}